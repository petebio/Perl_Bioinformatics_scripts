#!/usr/bin/env perl
use warnings;
use strict;

######################################################################################################################################################

while(my$line = <STDIN>){
	next if $. == 1;
	chomp $line;

	my@cols = split("\t", $line);

	my$chr = $cols[1];
	my$start = $cols[2] - 1;
	my$end = $cols[3];

	my$dist = $cols[9];
	my$gene_id = $cols[15];

	if($dist eq "" || $dist !~ m/^\d+/){
		$gene_id = 'No_Gene';
		$dist = 'NA';
	}
	elsif(abs($dist) > 100000){
		$dist = 'NA';
		$gene_id = 'No_Gene';
	}

	print STDOUT $chr,"\t",$start,"\t",$end,"\t",$dist,"\t",$gene_id,"\n";
}

######################################################################################################################################################

exit;

